const normalizeText = (value) => (value || '').toLowerCase();

const scoreByTerms = (text, terms) =>
  terms.reduce((score, term) => (text.includes(term) ? score + 1 : score), 0);

const uniqueById = (items) => {
  const seen = new Set();

  return items.filter((item) => {
    if (!item?.id || seen.has(item.id)) {
      return false;
    }

    seen.add(item.id);
    return true;
  });
};

const rankItems = (items, textBuilder, terms, limit) => {
  const ranked = uniqueById(items)
    .map((item) => ({
      item,
      score: scoreByTerms(normalizeText(textBuilder(item)), terms),
    }))
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score)
    .map(({ item }) => item);

  return ranked.slice(0, limit);
};

const buildProjectText = (project) => [
  project.title,
  project.summary,
  project.excerpt,
  project.description,
  ...(project.types || []).map((type) => type?.name),
  ...(project.categories || []).map((category) => category?.name),
  project.client?.name,
].filter(Boolean).join(' ');

const buildPostText = (post) => [
  post.title,
  post.excerpt,
  ...(post.tags || []),
  ...(post.categories || []).map((category) => category?.name),
].filter(Boolean).join(' ');

export const getServiceSeoContent = (serviceSlug) =>
  serviceSeoContent[serviceSlug] || defaultServiceContent;

export const getServiceTerms = (serviceSlug) => {
  if (!serviceSlug) {
    return [];
  }

  return serviceTerms[serviceSlug] || [serviceSlug.replace(/-/g, ' ')];
};

export const pickRelatedPostsForService = (serviceSlug, posts, limit = 3) => {
  if (!serviceSlug) {
    return uniqueById(posts).slice(0, limit);
  }

  const terms = getServiceTerms(serviceSlug);
  const ranked = rankItems(posts, buildPostText, terms, limit);

  if (ranked.length >= limit) {
    return ranked;
  }

  return uniqueById([...ranked, ...posts]).slice(0, limit);
};

