export const pickRelatedServicesForPost = (post, services, limit = 3) => {
  if (!post) {
    return uniqueById(services).slice(0, limit);
  }

  const postText = normalizeText(buildPostText(post));

  const ranked = uniqueById(services)
    .map((service) => {
      const terms = getServiceTerms(service.slug);
      const titleScore = scoreByTerms(postText, terms);
      const textScore = scoreByTerms(
        normalizeText(`${service.title} ${service.short_description || ''}`),
        terms
      );

      return {
        service,
        score: titleScore + textScore,
      };
    })
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score)
    .map(({ service }) => service);

  if (ranked.length >= limit) {
    return ranked.slice(0, limit);
  }

  return uniqueById([...ranked, ...services]).slice(0, limit);
};

export const pickRelatedProjectsForPost = (post, projects, limit = 2) => {
  if (!post) {
    return uniqueById(projects).slice(0, limit);
  }

  const terms = Array.from(
    new Set(
      [
        post?.title,
        post?.excerpt,
        ...(post?.tags || []),
        ...((post?.categories || []).map((category) => category?.name)),
      ]
        .filter(Boolean)
        .flatMap((value) => normalizeText(value).split(/[^a-z0-9]+/).filter((term) => term.length > 3))
    )
  );

  const ranked = rankItems(projects, buildProjectText, terms, limit);

  if (ranked.length >= limit) {
    return ranked;
  }

  return uniqueById([...ranked, ...projects]).slice(0, limit);
};
